package domain;
//while = enquanto (repete os dados conforme a lógica passada)
//incrementação = vai adicionando o valor informado até o maximo do valor passado na condição

public class HelloWorld {

    public static void main(String[] args) {
        
        int contador = 0;
        
        while(contador <= 5){
            System.out.println(contador);
            contador += 1; //incrementação
        }

    }

}
